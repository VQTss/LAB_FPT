public class ArrayObject {

    private int[] arr;

    /**
     * Tạo mới 1 array
     *
     * @param arr
     */
    public ArrayObject(int[] arr) {
        this.arr = arr;
    }

    /**
     * Lấy array
     *
     * @return
     */
    public int[] getArr() {
        return arr;
    }

    /**
     * set một array
     *
     * @param arr
     */
    public void setArr(int[] arr) {
        this.arr = arr;
    }

}
